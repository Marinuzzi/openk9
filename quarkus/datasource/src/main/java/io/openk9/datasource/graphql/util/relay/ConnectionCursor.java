package io.openk9.datasource.graphql.util.relay;

import graphql.Assert;

import java.util.Objects;

public class ConnectionCursor {

    private final String value;

    public ConnectionCursor(String value) {
        Assert.assertTrue(value != null && !value.isEmpty(), () -> "connection value cannot be null or empty");
        this.value = value;
    }


    public String getValue() {
        return value;
    }

    @Override
    public boolean equals(Object o) {
        if (this == o) {
            return true;
        }
        if (o == null || getClass() != o.getClass()) {
            return false;
        }
        ConnectionCursor that = (ConnectionCursor) o;
        return Objects.equals(value, that.value);
    }

    @Override
    public int hashCode() {
        return Objects.hashCode(value);
    }

    @Override
    public String toString() {
        return value;
    }

    public static ConnectionCursor fromString(String value) {
        return new ConnectionCursor(value);
    }

}
